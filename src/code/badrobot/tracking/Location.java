package code.badrobot.tracking;

/**
 *
 * @author chris
 */
public class Location {

    private double x;
    private double y;
    private double s;

    public double getS() {
        return s;
    }

    public void setS(double s) {
        this.s = s;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

}
